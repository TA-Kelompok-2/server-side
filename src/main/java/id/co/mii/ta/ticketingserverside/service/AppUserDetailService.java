package id.co.mii.ta.ticketingserverside.service;

import id.co.mii.ta.ticketingserverside.model.AppUserDetail;
import id.co.mii.ta.ticketingserverside.model.User;
import id.co.mii.ta.ticketingserverside.repository.UserRepository;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;

/**
 *
 * @author Fathullah
 */
@Service
public class AppUserDetailService implements UserDetailsService {

    private UserRepository userRepository;

    @Autowired
    public AppUserDetailService(UserRepository userRepository) {
        this.userRepository = userRepository;
    }

    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {
        User user = userRepository.findByUsername(username)
                .orElseThrow(() -> new UsernameNotFoundException("Username or Password Incorrect"));
        return new AppUserDetail(user);
    }
}
